public class MixMatch{
    public static void main(String[] args){
    String rand = "Java";
    System.out.println(rand + ": String - Stores a sequence of characters.");
    char rand2 = 'J';
    System.out.println(rand2 + ": Char - Stores a single character.");
    short a = 1;
    System.out.println(a + ": Short - Stores whole numbers.");
    int b = 2;
    System.out.println(b + ": Int - Stores whole number, without the decimal component.");
    long c = 3456789;
    System.out.println(c + ": Long - Stores large whole numbers.");
    float x = 0.1f;
    System.out.println(x + ": Float - Stores decimal numbers.");
    double in_java = 100.0;
    System.out.println(in_java + ": Double - Stores decimal numbers, upto 15.");
    boolean choice = true;
    System.out.println(choice + ": Boolean - Stores only boolean variables such as true or false.");
    }
}